public class Zavd1 {
    public static void main(String[] args) {
        // ініціалізуємо 10 цілих змінних
        int num1 = 5;
        int num2 = 10;
        int num3 = 15;
        int num4 = 20;
        int num5 = 25;
        int num6 = 30;
        int num7 = 35;
        int num8 = 40;
        int num9 = 45;
        int num10 = 50;

        // додамо їх між собою
        int sum = num1 + num2 + num3 + num4 + num5 + num6 + num7 + num8 + num9 + num10;
        System.out.println("Сума = " + sum);

        // віднімемо їх між собою
        int diff = num1 - num2 - num3 - num4 - num5 - num6 - num7 - num8 - num9 - num10;
        System.out.println("Різниця = " + diff);

        // перемножимо їх між собою
        int prod = num1 * num2 * num3 * num4 * num5 * num6 * num7 * num8 * num9 * num10;
        System.out.println("Добуток = " + prod);

        // розділимо їх між собою
        int div = num1 / num2 / num3 / num4 / num5 / num6 / num7 / num8 / num9 / num10;
        System.out.println("Частка = " + div);
    }
}
